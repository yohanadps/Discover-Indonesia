                    Olympic dan sprint akan menjadi kompetisi terbuka.
                    Olympic mencakup renang 1,5 km, 40 km bersepeda dan 10 km lari.
                    Nomor sprint terdiri dari 750 m renang, 20 km bersepeda dan 5 km lari.
                    Youth distance meliputi 300 m renang, 12 km bersepeda dan 3 km lari.
                    Kids distance terdiri dari 150 m renang, 6 km bersepeda dan 1,5 km lari.
                    Sementara  fun race duo mencakup 1,5 km lari, 150 renang dan sedikit rintangan ke garis  finish. 
                    Renang akan diselenggarakan di perairan biru dan jernih Laut Cina Selatan, tepatnya di lepas pantai Nirwana Gardens.
