            <h2>Kegiatan</h2>
            <p>Sebelum menuju puncak monumen, Anda akan disajikan 51 diorama di lantai dasarnya yang dikenal sebagai Museum Sejarah Nasional Indonesia. Museum berukuran 80x80 meter ini memperlihatkan sejarah Indonesia mulai dari masa pra sejarah hingga masa pemerintahan orde baru. </p>
            <p>Di dalam cawan monumen terdapat ruang kemerdekaan yang menampilkan Naskah Teks Proklamasi Kemerdekaan Indonesia dalam sebuah kotak kaca. Ada pula lambang negara Indonesia, peta kepulauan Indonesia yang berlapis emas, bendera merah putih, dan dinding yang bertuliskan Naskah Proklamasi Indonesia. </p> 
            <p>Anda dapat menggunakan liftuntuk menuju puncak Monumen Nasional. Pelatarannya dapat menampung 50 orang dan tersedia teropong untuk menikmati pemandangan seluruh sudut Kota Jakarta. Saat cuaca cerah dimungkinkan untuk melihat Gunung Salak dan Kepulauan Seribu.</p>
            <img src="http://www.layoverguide.com/wp-content/uploads/2010/10/Monas-park-Jakarta-Indonesia.jpg">
            <p>Di sekeliling Monas terdapat air mancur yang berliuk-liuk sesuai alunan musik. Kolam tersebut menarik untuk dinikmati pada malam hari. Terkadang dalam acara tertentu dipertunjukkan pula laser berwarna-warni di bagian air mancurnya dan tugunya disorot lampu dari bawah.</p>
            <p>Di Taman Monas seluas 80 hektar berkeliaran rusa-rusa di antara pepohonan rindang. Taman ini terbuka gratis untuk umum. Di bagian selatannya tersedia taman refleksi, yaitu batu-batuan yang dapat diinjak untuk pijat refleksi.</p> 
            <p>Anda dapat pula berolahraga di kawasan ini karena tersedia beberapa lapangan futsal dan basket yang bisa digunakan siapapun. </p>
