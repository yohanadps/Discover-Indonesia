                <div class="deskripsi">
                    <p>Inilah monumen yang telah menjadi ikonnya kota Jakarta sekaligus juga pengenang perjuangan rakyat Indonesia dalam merebut kemerdekaan dari belenggu penjajahan. Berlokasi di Lapangan Monas, Jakarta Pusat, Monumen Nasional atau sering disebut Monas didirikan tahun 1959 namun baru diresmikan pada 17 Agustus 1961.</p>
            <p>Di dalam Monumen Nasional terdapat museum yang menyajikan diorama perjuangan rakyat Indonesia dalam merebut kemerdekaan. Tersimpan pula teks asli Naskah Proklamasi Kemerdekaan. Di bagian puncak monumennya Anda dapat melihat pemandangan kota Jakarta dengan bangunan pencakar langit ibarat hutan beton yang menawarkan pemandangan lampu malam yang indah.</p>
<img src="http://www.indonesia.travel/public/media/images/upload/poi/Monumen%20Nasional%20(41)2014TMII.jpg">
            <p>Monas memiliki tinggi 132 meter dengan bentuk obelisk dan ada pula yang menyebut bentuknya adalah lingga yang digandengkan dengan yonidi puncaknya. Seluruh bangunan Monas dilapisi oleh marmer dengan puncaknya berupa cawan dengan 77 bagian lidah api yang disatukan setinggi 17 meter dengan diameter 6 meter.  Lidah api tersebut berbahan perunggu seberat 14,5 ton yang dilapisi emas 45 kg. Nyala lidah api itu merupakan simbol semangat perjuangan rakyat Indonesia yang tidak pernah padam untuk meraih kemerdekaan.</p> 
            <p>Emas di bagian pucuk Monas adalah sumbangan seorang pengusaha pribumi asal Aceh yaitu Teuku Markam. emas tersebut diambil dari tambang yang berloksi di Lebong, Provinsi Bengkulu. Sebelumnya Pemerintah Hindia Belanda menjadikan Bengkulu sebagai tambang emas dan menyebutnya sebagai Batavia II. Sumber lain menyebut bahwa Kerajaan Belanda saat itu menukar Singapura yang dikuasainya dengan Bengkulu kepada Kerajaan Inggris karena alasan tambang emas di Bengkulu.</p>
            <img src="http://krjogja.com/thumbhead/271c45f087241c3507bf9822bf53cbc2_thumb.jpg">
            <p>Monas begitu identik dengan Presiden Soekarno karena memang rancangan pendirian monumen ini diawali oleh sayembara dan dinilai langsung oleh beliau. Adalah Friederich Silaban yang memikat Soekarno dengan konsep obelisk. Akan tetapi, saat awal pembangunan berjalan kemudian Soekarno yang juga seorang arsitek mengubah dan mengalihkan konsep pembangunannya dengan melibatkan seorang arsitek bernama Soedarsono dengan konsultan Ir. Rooseno.  Saat awal pembangunan Monas terkendala keuangan mengingat Soekarno juga berambisi membangun masjid menjadi yang terbesar se-Asia Tenggara (Masjid Istiqlal).</p>
            <p>Pada masa Pemerintahan Hindia Belanda, kawasan sekitaran Monas adalah pusat bangunan penting pemerintahan yang dinamakan Koningsplein. Di bagian utaranya berdiam rumah Gubernur Jenderal Hindia Belanda yang kini menjadi Istana Merdeka.</p>
            <p>Di sekitaran Monas awalnya adalah taman hutan kota yang dahulu dikenal dengan nama Lapangan Gambir. Namanya kemudian berubah beberapa kali menjadi Lapangan Ikada, Lapangan Merdeka, Lapangan Monas, dan terakhir Taman Monas.</p>
            <p>Untuk masuk ke area Monas maka Anda perlu membeli tiket Rp20.000,- untuk dewasa dan Rp10.000,- untuk anak-anak. Tiket dibeli di loket yang terdapat di pintu sebelah utara Monas. Sedangkan tiket lift untuk masuk ke pelataran puncaknya seharga Rp7.500,- untuk dewasa dan Rp3.500,- untuk anak-anak. Jam buka Monas adalah pukul 9.00 pagi hingga 16.00 sore.</p>
