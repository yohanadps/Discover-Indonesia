                <p>Terletak di bagian barat lereng Gunung Agung dan disebut sebagai pura Hindu terbesar di Bali. Pura Besakih terletak di daerah luas, menawarkan kepada Anda pemandangan indah dan bangunan pura menawan yang mengelilingi komplek pura ini.</p>
            <p>Candi Besakih terletak di Desa Besakih, di bagian timur Bali. Nama Besakih berasal dari kata "Basuki", diambil dari kata "Wasuki" yang berarti Keselamatan dalam bahasa Sansekertanya. Sedangkan, dalam mitologi Samudramanthana, nama "Besuki" sebenarnya mengacu pada  "Naga Besukian", yang mendiami Gunung Agung, gunung berapi utama di Bali.</p>
