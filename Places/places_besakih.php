            <img src="http://www.indonesia.travel/public/media/images/upload/poi/besakih%202.jpg">
            <p>Besakih adalah candi yang luar biasa, lihatlah candi dari jarak jauh maka Anda dapat menyaksikan sebuah komplek candi seperti piramida yang mengesankan, membentang hingga ke gunung, dengan Gunung Agung sebagai latar belakangnya yang dramatis. Pemandangan  menakjubkan adalah  pada pagi hari di mana cahaya mentari pagi menyinari komplek pura dan saat matahari terbenam. Pura ini juga dihiasi dengan ornamen bergaya Bali, tangga pura, berhala batu suci, dan ukiran batu.</p>
            <p>Sebelum menjelajahi pura ini, Anda harus menyewa dan memakai sarung di pintu masuk utama kuil untuk menghormati tradisi Bali, karena Pura Besakih adalah tempat ibadah yang suci. Sarong dan selempang adalah pakaian khusus orang Bali saat berdoa di kuil.</p>
