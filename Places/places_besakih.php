            <p>Ketika Anda mencapai area komplek, Anda akan melihat pura pertama di sebelah kiri. Pura ini adalah bait suci yang didedikasikan untuk nenek moyang kerajaan yang dikenal sebagai Pura Dalem Puri. Orang Bali menyimpan abu ngaben di dalam pura ini. Dari sini, berjalan menaiki tangga yang akan membawa Anda ke Pura Penataran Agung.</p>
            <p>Penduduk setempat percaya bahwa Pura Penataran Agung adalah pusat dari pengabdian kepada para Dewa. Di samping candi utama ada dua candi penting lainnya., yaitu Pura Kiduling Kreteg di sebelah kanan dan Pura Batu Madeg di kiri.</p>
            <p>Warna-warna kain pada pura didedikasikan untuk sang Pencipta, yang mewakili Trinitas Hindu. Bendera putih didedikasikan untuk candi Siwa (Pura Penataran Agung), banner merah adalah candi Brahma (Pura Kiduling Kreteg), sedangkan banner hitam adalah bait Wisnu (Pura Batu Mandeg). Bila sedang tidak ada festival di pura ini, pengunjung mungkin diperbolehkan masuk ke dalam melihat-lihat candi.</p>
            <p>Jika Anda ingin menjelajahi bagian pura yang lebih terpencil, pergilah menuju Pura Pengubengan, yang terletak sekitar 2 kilometer di ujung utara komplek.</p>
